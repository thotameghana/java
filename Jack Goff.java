import java.util.*;
import java.io.*;
import java.math.*;
class Solution {

    public static void main(String args[]) {
        Scanner in = new Scanner(System.in);
        int rb = in.nextInt();
        int cb = in.nextInt();
        int re = in.nextInt();
        int ce = in.nextInt();
        int a=(rb+re)/2;
        int b=(cb+ce)/2;
        System.out.print(a+" ");
        System.out.print(b);
    }
}
