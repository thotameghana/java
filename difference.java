import java.util.Scanner;
public class Main
{
    public static void main(String args[])
    {
        Scanner sc=new Scanner(System.in);
        int n=sc.nextInt();
        int a=sc.nextInt();
        int c=n-a;
        if(n>0 && a>0)
        {
            System.out.println(c);
        }
        else
        {
            System.out.println(c);
        }
    }
}
