import java.util.*;
import java.io.*;
import java.math.*;
class Solution {

    public static void main(String args[]) {
        Scanner in = new Scanner(System.in);
        int N = in.nextInt();
        for(int i=1;i<=N;i++)
        {
            for(int j=i;j<=N;j++)
            {
                System.out.print(N);
            }
            System.out.println();
        }
    }
}
